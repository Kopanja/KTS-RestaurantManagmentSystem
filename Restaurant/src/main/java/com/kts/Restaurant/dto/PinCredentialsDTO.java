package com.kts.Restaurant.dto;

public class PinCredentialsDTO {
	
	String pin;

	public PinCredentialsDTO() {
		super();
	}

	public PinCredentialsDTO(String pin) {
		super();
		this.pin = pin;
	}

	public String getPin() {
		return pin;
	}

	public void setPin(String pin) {
		this.pin = pin;
	}
	
	

}
